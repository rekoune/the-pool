#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (av[ac - 1] != av[0] && ac > 1)
	{
		i = 0;
		while (av[ac - 1][i] != 0)
		{
			write(1, &av[ac - 1][i], 1);
			i++;
		}
		write(1, "\n", 1);
		ac--;
	}
}
